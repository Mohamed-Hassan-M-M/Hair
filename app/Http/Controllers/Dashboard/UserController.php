    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        try{
            $user = User::find($id);
            if(!$user){
                return redirect()->route('user.index')->with(['error'=>'This user does not exist']);
            }
            $user->delete();
            return redirect()->route('user.index')->with(['success'=>'This user deleted successfully']);
        }catch (\Exception $ex){
            return redirect()->route('user.index')->with(['error'=>'Error, Try again later']);
        }
    }
